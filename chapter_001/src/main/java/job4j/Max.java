package job4j;
/**
* Класс для нахождения максимального числа.
* @author dlukyanchuk
* @since 13.02.2017
* @version 1
*/
public class Max {
/**
* Создание класса Max.
* @param a первое число
* @param b второе число
* @return результат
*/
	public int max(int a, int b) {
/**
* Создание метода макс.
* @param a первое число
* @param b второе число
* @return результат
*/
	return a >= b ? a : b;
}
};